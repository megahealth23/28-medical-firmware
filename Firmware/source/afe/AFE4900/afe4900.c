static int8_t AFE4900_I2C_connect_verify(AFE4900_addr_t addr, uint32_t verify_val, bool is_rewrite, struct afe4900_dev *dev)
{
	int8_t rslt = AFE4900_OK;
	uint32_t tmp_wval = verify_val, tmp_rval;
    uint8_t rxdata[3] = { 0 }, txdata[3] = { 0 };
	
    if ( ( NULL == dev ) || ( NULL == dev->read ) || ( NULL == dev->write ) ) 
	{
		rslt = AFE4900_E_NULL_PTR;
	}		
	else
	{
		if(is_rewrite) 
		{					
			txdata[0] = (uint8_t)(tmp_wval >> 16);
			txdata[1] = (uint8_t)(tmp_wval >> 8);
			txdata[2] = (uint8_t)(tmp_wval);
			rslt = dev->write(dev->id, addr, &txdata[0], 3);
			dev->delay_ms(5);
		}
		
		rslt = dev->read(dev->id, addr, &rxdata[0], 3);
		tmp_rval = (rxdata[0]<<16)+(rxdata[1]<<8)+(rxdata[2]);	
				
		if( tmp_wval == tmp_rval )
			rslt = AFE4900_OK;
		else
			rslt = AFE4900_E_DEV_NOT_FOUND;
	}

    return rslt;	
}

/*    Analog-to-Digital Converter (PPG and ECG Signal Chain)   */
static int32_t afe4900_convert_sensor_data(uint8_t *rxdata, uint8_t len, bool local_convert)
{
	// afe4900 datasheet 
	int32_t data;
    int32_t temp = 0;
    uint8_t symbol;
		
	if( (3 == len) || (NULL != rxdata) )
	{
		data = (rxdata[0]<<16)+(rxdata[1]<<8)+(rxdata[2]);	
		symbol = (data>>21)&0x07;
	}
	else
		symbol = 0x06;
		
	if(local_convert)
	{		
		if(0x00 == symbol)  /*Positive and lower than positive full-scale (within full-scale range)*/
		{
			temp = data&0x1FFFFF;
		}
		else if(0x07 == symbol) /*Negative and higher than negative full-scale (within full-scale range)*/
		{
			temp = (int32_t)(data | 0xFF000000);
		}
		else if(0x01 == symbol)  /*Positive and higher than positive full-scale (outside full-scale range)*/
		{
			temp = 0x200000;
		}
		else if(0x06 == symbol) /*Negative and lower than negative full-scale (outside full-scale range)*/
		{
			temp = 0x200000;
			temp = - temp;
		}
	}else 
		temp = data;

    return temp;
}

