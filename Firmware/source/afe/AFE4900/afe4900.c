/*!
 * @brief This API configures the power mode, range and bandwidth
 * of sensor.
 */
int8_t afe4900_set_sens_conf(struct afe4900_dev *dev)
{
    int8_t rslt = AFE4900_OK;
	
    /* Null-pointer check */
    if ((NULL == dev) || ( NULL == dev->sw_reset))
    {
        rslt = AFE4900_E_NULL_PTR;
    }
    else
    {			
		dev->sw_reset();
		switch(dev->dev_mode & AFE4900_DEV_MAJOR_MODE_MASK)
		{
			case AFE4900_DEV_POWER_DOWN_MODE:
				rslt = set_pd_conf(dev);
				break;
				
			case AFE4900_DEV_PPG_ONLY_MODE:
				rslt = set_ppg_conf(dev->dev_mode & AFE4900_DEV_MINOR_MODE_MASK, dev);
				break;		
				
			case AFE4900_DEV_PTT_MODE:
				rslt = set_ptt_conf(dev);
				break;
			default:
				rslt = AFE4900_E_COM_FAIL;
				break;
		}
    }

    return rslt;
}

