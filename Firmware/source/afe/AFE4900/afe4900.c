/*!
 * @brief This API set the  mode configuration.
 */
static int8_t set_ppg_conf(uint16_t minjor, struct afe4900_dev *dev)
{
	extern bool isFTdone();

	int8_t rslt = AFE4900_OK;
	uint8_t ite = 0;
	uint8_t txdata[3] = { 0 };
	uint8_t init_ovtime = 5;  // define times used to rewrite;
			
	if((NULL == dev))
	{
		rslt = AFE4900_E_NULL_PTR;
	}
	else
	{
		switch(minjor)
		{
			case AFE4900_DEV_PPG_SPO2_MODE:

				dev->ppg_cfg.cfg_cnt = PPG_PARM_CNT;
				dev->ppg_cfg.addrs = ppg_m_addrs;
				dev->ppg_cfg.regs = ppg_spo2_m_vals;
				break;
			case AFE4900_DEV_PPG_SPORT_MODE:

				dev->ppg_cfg.cfg_cnt = PPG_PARM_CNT;
				dev->ppg_cfg.addrs = ppg_m_addrs;
				dev->ppg_cfg.regs = ppg_ehr_m_vals;

				break;
			// case AFE4900_DEV_PPG_PULSE_MODE:
			// 	dev->ppg_cfg.cfg_cnt = PPG_PARM_CNT;
			// 	dev->ppg_cfg.addrs = ppg_m_addrs;
			// 	dev->ppg_cfg.regs = ppg_pulse_m_vals;

			// 	break;
			case AFE4900_DEV_PPG_DAILY_MODE:
				dev->ppg_cfg.cfg_cnt = PPG_PARM_CNT;
				dev->ppg_cfg.addrs = ppg_m_addrs;
				dev->ppg_cfg.regs = ppg_daily_m_vals;
				break;
			case AFE4900_DEV_PPG_BP_MODE:
				dev->ppg_cfg.cfg_cnt = PPG_PARM_CNT;
				dev->ppg_cfg.addrs = ppg_m_addrs;
				dev->ppg_cfg.regs = ppg_bp_m_vals;
				break;
		}
	}
			
	if( (NULL == dev) || (NULL == dev->read) || (NULL == dev->write) || (NULL == dev->ppg_cfg.addrs) || (NULL == dev->ppg_cfg.regs) )
	{
		rslt = AFE4900_E_NULL_PTR;
	}
	else
	{
		do
		{
			for(ite = 0; ite < dev->ppg_cfg.cfg_cnt; ite++)
			{
				txdata[0] = (uint8_t)( dev->ppg_cfg.regs[ite] >> 16);
				txdata[1] = (uint8_t)( dev->ppg_cfg.regs[ite] >> 8);
				txdata[2] = (uint8_t)( dev->ppg_cfg.regs[ite] );
				rslt = dev->write(dev->id, dev->ppg_cfg.addrs[ite], &txdata[0], 3);
			}
			if( AFE4900_OK == (rslt = AFE4900_I2C_connect_verify( dev->ppg_cfg.addrs[PPG_MODE_TEST_RWREG1_ID],  dev->ptt_cfg.regs[PPG_MODE_TEST_RWREG1_ID], false, dev) )
					&& AFE4900_OK == (rslt = AFE4900_I2C_connect_verify( dev->ppg_cfg.addrs[PPG_MODE_TEST_RWREG1_ID],  dev->ptt_cfg.regs[PPG_MODE_TEST_RWREG1_ID], false, dev) ))
				break;
								
			init_ovtime--;
		}while( init_ovtime );

		if(0 == init_ovtime)
			rslt = AFE4900_E_DEV_NOT_FOUND;
	}
	return rslt;
}


